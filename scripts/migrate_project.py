def compose_bind_mounts(service: dict[str, Any]) -> list[tuple[str, str]]:
    mounts: list[tuple[str, str]] = []
    for mount in service.get("volumes") or []:
        source = None
        target = None
        if isinstance(mount, dict):
            if str(mount.get("type", "bind")) not in {"bind", ""}:
                continue
            source = mount.get("source")
            target = mount.get("target")
        else:
            parts = str(mount).split(":")
            if len(parts) >= 2:
                source, target = parts[0], parts[1]
        if not source or not target:
            continue
        source_text = str(source)
        if source_text.startswith(("/", "./", "../", "~")):
            mounts.append((source_text, str(target)))
    return mounts


def resolve_bind_source(args: argparse.Namespace, record: import_project.ServiceRecord, source: str) -> Path | None:
    project_path = Path(args.project_path).expanduser().resolve()
    compose_path = (project_path / record.file).resolve()
    source_path = Path(source).expanduser()
    if not source_path.is_absolute():
        candidate = (compose_path.parent / source_path).resolve()
        return candidate if candidate.exists() else None
    if source_path.exists():
        return source_path
    parts = source_path.parts
    for index, part in enumerate(parts):
        if part == project_path.name:
            candidate = project_path.joinpath(*parts[index + 1 :]).resolve()
            if candidate.exists():
                return candidate
    return None


def configmap_key(path: Path, used: set[str]) -> str:
    base = re.sub(r"[^A-Za-z0-9_.-]+", "-", path.as_posix()).strip(".-") or "file"
    key = base[:253]
    if key not in used:
        used.add(key)
        return key
    digest = hashlib.sha256(path.as_posix().encode("utf-8")).hexdigest()[:8]
    key = f"{base[:244]}-{digest}".strip(".-")
    used.add(key)
    return key


def configmap_file_entry(path: Path) -> tuple[str, str] | None:
    try:
        content = path.read_bytes()
    except OSError:
        return None
    if len(content) > CONFIGMAP_FILE_MAX_BYTES:
        return None
    if b"\0" in content:
        return ("binary", base64.b64encode(content).decode("ascii"))
    try:
        return ("text", content.decode("utf-8"))
    except UnicodeDecodeError:
        return ("binary", base64.b64encode(content).decode("ascii"))


def service_looks_like_dotnet(record: import_project.ServiceRecord, service: dict[str, Any]) -> bool:
    image = (record.image.display if record.image else "").lower()
    if any(token in image for token in ["dotnet", "aspnet", "mcr.microsoft.com"]):
        return True
    env_keys = {
        key.upper()
        for key, value in import_project.environment_entries(service.get("environment"))
        if value is not None
    }
    return bool(
        env_keys
        & {
            "ASPNETCORE_HTTP_PORTS",
            "ASPNETCORE_URLS",
            "ASPNET_VERSION",
            "DOTNET_RUNNING_IN_CONTAINER",
            "DOTNET_VERSION",
        }
    )


def merge_container_env(env: list[dict[str, str]], defaults: dict[str, str]) -> list[dict[str, str]]:
    existing = {entry["name"] for entry in env if "name" in entry}
    merged = list(env)
    for key, value in defaults.items():
        if key not in existing:
            merged.append({"name": key, "value": value})
    return merged


def default_import_environment(record: import_project.ServiceRecord, service: dict[str, Any]) -> dict[str, str]:
    # These are harmless for non-.NET workloads, and they stop imported .NET
    # services from exhausting shared host inotify limits in dense lab clusters.
    defaults: dict[str, str] = {
        "DOTNET_USE_POLLING_FILE_WATCHER": "true",
        "HOSTBUILDER__RELOADCONFIGONCHANGE": "false",
        "DOTNET_HOSTBUILDER__RELOADCONFIGONCHANGE": "false",
        "ASPNETCORE_HOSTBUILDER__RELOADCONFIGONCHANGE": "false",
    }
    return defaults


def apisix_config_text(record: import_project.ServiceRecord, target: str) -> bool:
    identity = " ".join(
        [
            record.name.lower(),
            record.kind.lower(),
            record.image.display.lower() if record.image else "",
            target.lower(),
        ]
    )
    return "apisix" in identity and "/conf/" in target


def normalize_imported_config_text(record: import_project.ServiceRecord, target: str, value: str) -> str:
    if apisix_config_text(record, target):
        value = re.sub(r"(?<![A-Za-z0-9.-])127[.]0[.]0[.]1:2379\b", "etcd:2379", value)
        value = re.sub(r"(?<![A-Za-z0-9.-])localhost:2379\b", "etcd:2379", value)
    return value


def should_mount_bind_as_configmap(record: import_project.ServiceRecord, target: str) -> bool:
    if record.kind == "nginx":
        return target == "/etc/nginx/nginx.conf" or target.startswith("/etc/nginx/") or target.startswith("/usr/share/nginx/html")
    return apisix_config_text(record, target)


def configmap_mount_manifests(
    args: argparse.Namespace,
    record: import_project.ServiceRecord,
    service: dict[str, Any],
    labels: dict[str, str],
) -> tuple[list[dict[str, Any]], list[dict[str, Any]], list[dict[str, Any]]]:
    manifests: list[dict[str, Any]] = []
    volumes: list[dict[str, Any]] = []
    volume_mounts: list[dict[str, Any]] = []
    for index, (source, target) in enumerate(compose_bind_mounts(service), start=1):
        if not should_mount_bind_as_configmap(record, target):
            continue
        if SECRET_ENV_RE.search(source) or SECRET_ENV_RE.search(target):
            continue
        source_path = resolve_bind_source(args, record, source)
        if source_path is None:
            continue
        cm_name = k8s_name(f"{kubernetes_workload_name(record)}-{index}", "import-cm")
        volume_name = f"cm-{index}"
        data: dict[str, str] = {}
        binary_data: dict[str, str] = {}
        items: list[dict[str, str]] = []
        used_keys: set[str] = set()
        total_size = 0
        if source_path.is_file():
            entry = configmap_file_entry(source_path)
            if entry is None:
                continue
            key = configmap_key(Path(source_path.name), used_keys)
            kind, value = entry
            if kind == "text":
                value = normalize_imported_config_text(record, target, value)
            total_size += len(value.encode("utf-8"))
            if kind == "text":
                data[key] = value
            else:
                binary_data[key] = value
            items.append({"key": key, "path": key})
            volume_mounts.append({"name": volume_name, "mountPath": target, "subPath": key, "readOnly": True})
        elif source_path.is_dir():
            for file_path in sorted(path for path in source_path.rglob("*") if path.is_file()):
                relative_path = file_path.relative_to(source_path)
                if SECRET_ENV_RE.search(relative_path.as_posix()):
                    continue
                entry = configmap_file_entry(file_path)
                if entry is None:
                    continue
                key = configmap_key(relative_path, used_keys)
                kind, value = entry
                if kind == "text":
                    value = normalize_imported_config_text(record, f"{target}/{relative_path.as_posix()}", value)
                total_size += len(value.encode("utf-8"))
                if total_size > CONFIGMAP_TOTAL_MAX_BYTES:
                    break
                if kind == "text":
                    data[key] = value
                else:
                    binary_data[key] = value
                items.append({"key": key, "path": relative_path.as_posix()})
            if not items:
                continue
            volume_mounts.append({"name": volume_name, "mountPath": target, "readOnly": True})
        else:
            continue
        config_map: dict[str, Any] = {
            "apiVersion": "v1",
            "kind": "ConfigMap",
            "metadata": {"name": cm_name, "namespace": args.namespace, "labels": labels},
        }
        if data:
            config_map["data"] = data
        if binary_data:
            config_map["binaryData"] = binary_data
        manifests.append(config_map)
        volumes.append({"name": volume_name, "configMap": {"name": cm_name, "items": items}})
    return manifests, volumes, volume_mounts


def kubernetes_service_manifest(
    *,
    name: str,
    namespace: str,
    labels: dict[str, str],
    ports: list[int],
    annotations: dict[str, str] | None = None,
) -> dict[str, Any]:
    metadata: dict[str, Any] = {"name": name, "namespace": namespace, "labels": labels}
    if annotations:
        metadata["annotations"] = annotations
    return {
        "apiVersion": "v1",
        "kind": "Service",
        "metadata": metadata,
        "spec": {
            "type": "ClusterIP",
            "selector": labels,
            "ports": [
                {
                    "name": kubernetes_service_port_name(port),
                    "port": port,
                    "targetPort": port,
                    "protocol": "TCP",
                }
                for port in ports
            ],
        },
    }


def kubernetes_workload_manifests(
    args: argparse.Namespace,
    service_pairs: list[tuple[import_project.ServiceRecord, dict[str, Any]]],
) -> list[dict[str, Any]]:
    manifests: list[dict[str, Any]] = []
    workload_inputs: list[tuple[import_project.ServiceRecord, dict[str, Any], str, list[int]]] = []
    generated_names: set[str] = set()
    alias_counts: dict[str, int] = {}

    for record, service in service_pairs:
        if not should_generate_kubernetes_workload(args, record, service):
            continue
        image = image_for_kubernetes_manifest(args, record)
        ports = kubernetes_container_ports(record, service)
        if not image or not ports:
            continue
        workload_inputs.append((record, service, image, ports))
        generated_names.add(kubernetes_workload_name(record))
        alias = compose_service_dns_alias(record)
        alias_counts[alias] = alias_counts.get(alias, 0) + 1

    for record, service, image, ports in workload_inputs:
        name = kubernetes_workload_name(record)
        generate_tcp_probes = should_generate_tcp_probes(record, service)
        probe_mode = "tcp" if generate_tcp_probes else "disabled-background-service"
        labels = {
            "app.kubernetes.io/name": name,
            "app.kubernetes.io/part-of": "urban-platform-import",
            "app.kubernetes.io/managed-by": "urban-platform-import",
        }
        env = [
            {"name": key, "value": value}
            for key, value in import_project.environment_entries(service.get("environment"))
            if value is not None and not SECRET_ENV_RE.search(key) and KUBERNETES_ENV_NAME_RE.fullmatch(key)
        ]
        env = merge_container_env(env, default_import_environment(record, service))
        container: dict[str, Any] = {
            "name": name,
            "image": image,
            "imagePullPolicy": "IfNotPresent",
            "ports": [{"name": kubernetes_service_port_name(port), "containerPort": port, "protocol": "TCP"} for port in ports],
        }
        if generate_tcp_probes:
            container["readinessProbe"] = {"tcpSocket": {"port": ports[0]}, "initialDelaySeconds": 15, "periodSeconds": 10}
            container["livenessProbe"] = {"tcpSocket": {"port": ports[0]}, "initialDelaySeconds": 45, "periodSeconds": 20}
        if env:
            container["env"] = env
        if secret_entries(service):
            container["envFrom"] = [{"secretRef": {"name": k8s_name(record.name, "import-env"), "optional": True}}]
        resources = imported_workload_resources(args)
        if resources:
            container["resources"] = resources
        config_map_manifests, volumes, volume_mounts = configmap_mount_manifests(args, record, service, labels)
        if volume_mounts:
            container["volumeMounts"] = volume_mounts
        pod_spec: dict[str, Any] = {
            "enableServiceLinks": False,
            "containers": [container],
        }
        if volumes:
            pod_spec["volumes"] = volumes
        annotations = {
            "urban-platform.io/compose-file": record.file,
            "urban-platform.io/compose-service": record.name,
            "urban-platform.io/migration-profile": args.profile,
            "urban-platform.io/probe-mode": probe_mode,
        }
        deployment = {
            "apiVersion": "apps/v1",
            "kind": "Deployment",
            "metadata": {
                "name": name,
                "namespace": args.namespace,
                "labels": labels,
                "annotations": annotations,
            },
            "spec": {
                "replicas": 1,
                "selector": {"matchLabels": labels},
                "template": {
                    "metadata": {"labels": labels, "annotations": {"urban-platform.io/probe-mode": probe_mode}},
                    "spec": pod_spec,
                },
            },
        }
        primary_service = kubernetes_service_manifest(name=name, namespace=args.namespace, labels=labels, ports=ports)
        manifests.extend(config_map_manifests + [deployment, primary_service])

        alias = compose_service_dns_alias(record)
        if alias != name and alias_counts.get(alias) == 1 and alias not in generated_names:
            manifests.append(
                kubernetes_service_manifest(
                    name=alias,
                    namespace=args.namespace,
                    labels=labels,
                    ports=ports,
                    annotations={
                        "urban-platform.io/compose-service-alias": record.name,
                        "urban-platform.io/alias-target": name,
                    },
                )
            )
    return manifests


