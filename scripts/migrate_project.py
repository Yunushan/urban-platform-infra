def preload_archives_to_nodes(args: argparse.Namespace, archives: list[Path], stale_archive_names: set[str], image_refs: list[str] | None = None) -> bool:
    targets, ssh_options = preload_node_targets(args)
    archives = sorted(archives)
    if not archives:
        print("No image archives were written in this run.")
        return False
    if not targets:
        archive_dir = archives[0].parent
        print(f"Image archives written to {archive_dir}. Set MIGRATION_RKE2_NODES to copy them to RKE2 nodes automatically.")
        return False
    remote_image_dir_raw = args.rke2_image_dir.rstrip("/")
    remote_image_dir = shlex.quote(remote_image_dir_raw)
    stale_names = sorted(stale_archive_names | {archive.name for archive in archives})
    stale_name_args = " ".join(shlex.quote(name) for name in stale_names)
    for target in targets:
        run_remote_sudo_shell(
            args,
            ssh_options,
            target,
            f"mkdir -p {remote_image_dir}; "
            f"rm -rf {remote_image_dir}/.urban-platform-import-tmp; "
            f"for name in {stale_name_args}; do rm -f {remote_image_dir}/\"$name\"; done",
        )
        for archive in archives:
            if image_refs and remote_containerd_has_image(args, ssh_options, target, image_refs):
                print(f"Image {image_refs[0]} is already present on {target}; skipping archive upload.")
                continue
            remote_archive = f"{remote_image_dir_raw}/{archive.name}"
            run_remote_sudo_upload(args, ssh_options, target, archive, remote_archive)
            if args.rke2_import_images:
                import_preloaded_archives_to_containerd(args, ssh_options, target, remote_image_dir, [archive.name])
    return True


def import_preloaded_archives_to_containerd(args: argparse.Namespace, ssh_options: list[str], target: str, remote_image_dir: str, archive_names: list[str]) -> None:
    archive_name_args = " ".join(shlex.quote(name) for name in archive_names)
    script = (
        "set -e; "
        "ctr=/var/lib/rancher/rke2/bin/ctr; "
        "socket=/run/k3s/containerd/containerd.sock; "
        f"image_dir={remote_image_dir}; "
        "if [ -S \"$socket\" ] && [ -x \"$ctr\" ]; then "
        f"for name in {archive_name_args}; do "
        "archive=\"$image_dir/$name\"; "
        "[ -e \"$archive\" ] || continue; "
        "\"$ctr\" --address \"$socket\" -n k8s.io images import \"$archive\"; "
        "rm -f \"$archive\"; "
        "done; "
        "\"$ctr\" --address \"$socket\" -n k8s.io images ls >/dev/null; "
        "echo \"Imported preload archives into running RKE2 containerd and removed staged tar files.\"; "
        "else "
        "echo \"RKE2 containerd is not running on this node; archives are present for startup import.\"; "
        "fi"
    )
    run_remote_sudo_shell(args, ssh_options, target, script)


def database_instances(values: dict[str, Any], namespace: str) -> list[dict[str, Any]]:
    instances = values.get("databases", {}).get("instances", {})
    targets: list[dict[str, Any]] = []
    for name, config in instances.items():
        if not isinstance(config, dict) or not config.get("enabled", False):
            continue
        engine = str(config.get("engine", "postgresql"))
        database = str(config.get("database", name))
        owner = str(config.get("owner", database))
        targets.append(
            {
                "name": str(name),
                "engine": engine,
                "host": f"{name}-rw.{namespace}.svc",
                "port": int(config.get("postgresPort", 5432)),
                "database": database,
                "owner": owner,
                "secretRef": {
                    "name": f"{name}-app",
                    "namespace": namespace,
                    "usernameKey": "username",
                    "passwordKey": "password",
                },
            }
        )
    return targets


def target_for_record(record: import_project.ServiceRecord, available: list[dict[str, Any]], index: int) -> dict[str, Any]:
    matching_index = next((i for i, target in enumerate(available) if target["engine"] == record.kind), None)
    if matching_index is None and record.kind == "postgresql":
        matching_index = next((i for i, target in enumerate(available) if target["engine"] in {"postgres", "postgresql"}), None)
    if matching_index is None and available:
        matching_index = 0
    if matching_index is None:
        return {
            "host": f"{k8s_name(record.name)}-rw.<namespace>.svc",
            "port": 5432,
            "database": "<target_database>",
            "username": "<target_user>",
            "password": "<target_password>",
        }
    return available.pop(matching_index)


def optional_database_target(record: import_project.ServiceRecord) -> dict[str, Any]:
    tools = OPTIONAL_DATABASE_TOOLS.get(record.kind, {})
    entry: dict[str, Any] = {
        "sourceAlias": k8s_name(record.name),
        "engine": record.kind,
        "targetMode": "<external|operator|managed>",
        "host": "<target-host>",
        "database": "<target-database>",
        "username": "<target-user>",
        "password": "<target-password>",
        "migration": {
            "status": "scaffolded",
            "dumpTool": tools.get("dump", "<engine-dump-tool>"),
            "restoreTool": tools.get("restore", "<engine-restore-tool>"),
        },
    }
    if record.kind in OPTIONAL_DATABASE_PORTS:
        entry["port"] = OPTIONAL_DATABASE_PORTS[record.kind]
    if record.kind == "sqlite":
        entry.update(
            {
                "targetMode": "<single-pod-pvc|externalized-service>",
                "filePath": "<sqlite-file-path>",
                "note": "SQLite is not an HA database. Use only for dev/single-pod mode or migrate the application to an external database.",
            }
        )
        entry.pop("host", None)
        entry.pop("port", None)
        entry.pop("username", None)
        entry.pop("password", None)
    return entry


def generated_database_targets(
    service_pairs: list[tuple[import_project.ServiceRecord, dict[str, Any]]],
    values: dict[str, Any],
    namespace: str,
) -> dict[str, Any]:
    available = database_instances(values, namespace)
    mapping: dict[str, Any] = {}
    database_records = [
        record
        for record, _service in service_pairs
        if record.kind in DATABASE_KINDS
    ]
    for index, record in enumerate(database_records):
        if record.kind in OPTIONAL_DATABASE_KINDS:
            mapping[record.name] = optional_database_target(record)
            continue
        target = target_for_record(record, available, index)
        entry = {
            "sourceAlias": k8s_name(record.name),
            "engine": record.kind,
            "targetService": target.get("name", "<target-service>"),
            "host": target["host"],
            "port": target["port"],
            "database": target["database"],
        }
        if target.get("secretRef"):
            entry["secretRef"] = target["secretRef"]
        if target.get("username"):
            entry["username"] = target["username"]
        if target.get("password"):
            entry["password"] = target["password"]
        mapping[record.name] = entry
    return {"databaseTargets": mapping}


