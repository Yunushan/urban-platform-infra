def docker_image_exists(image: str) -> bool:
    return subprocess.run(["docker", "image", "inspect", image], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL).returncode == 0


def pullable_image(image: import_project.ImageRef) -> bool:
    if image.variable:
        return False
    first_segment = image.repository.split("/", 1)[0]
    return "/" in image.repository or "." in first_segment or ":" in first_segment


def ensure_source_image(record: import_project.ServiceRecord) -> bool:
    if record.image is None:
        return False
    source = record.image.display
    if docker_image_exists(source):
        return True
    if pullable_image(record.image):
        print(f"Source image {source} is not local; pulling it before import.")
        pull = subprocess.run(["docker", "pull", source])
        return pull.returncode == 0
    print(f"Source image {source} is not local and does not look pullable; skipping {record.name}.")
    return False


def preload_archives_to_nodes(args: argparse.Namespace, archive_dir: Path) -> None:
    nodes = [node.strip() for node in args.rke2_nodes.split(",") if node.strip()]
    archives = sorted(archive_dir.glob("*.tar"))
    if not archives:
        print(f"No image archives were written to {archive_dir}.")
        return
    if not nodes:
        print(f"Image archives written to {archive_dir}. Set MIGRATION_RKE2_NODES to copy them to RKE2 nodes automatically.")
        return
    ssh_target_prefix = f"{args.ssh_user}@"
    ssh_options: list[str] = []
    if args.ssh_key:
        ssh_options.extend(["-i", args.ssh_key])
    remote_tmp = "/tmp/urban-platform-import-images"
    remote_image_dir = shlex.quote(args.rke2_image_dir)
    for node in nodes:
        target = node if "@" in node else f"{ssh_target_prefix}{node}"
        run_command(["ssh", *ssh_options, target, f"mkdir -p {remote_tmp}"])
        for archive in archives:
            run_command(["scp", *ssh_options, str(archive), f"{target}:{remote_tmp}/"])
        run_command(
            [
                "ssh",
                *ssh_options,
                target,
                f"sudo mkdir -p {args.rke2_image_dir} && sudo cp {remote_tmp}/*.tar {args.rke2_image_dir}/",
            ]
        )
        archive_checks = " ".join(shlex.quote(f"{args.rke2_image_dir}/{archive.name}") for archive in archives)
        run_command(["ssh", *ssh_options, target, f"sudo sh -lc 'for archive in {archive_checks}; do test -s \"$archive\"; done'"])
        if args.rke2_import_images:
            import_preloaded_archives_to_containerd(args, ssh_options, target, remote_image_dir)


def import_preloaded_archives_to_containerd(args: argparse.Namespace, ssh_options: list[str], target: str, remote_image_dir: str) -> None:
    script = (
        "set -e; "
        "ctr=/var/lib/rancher/rke2/bin/ctr; "
        "socket=/run/k3s/containerd/containerd.sock; "
        f"image_dir={remote_image_dir}; "
        "if [ -S \"$socket\" ] && [ -x \"$ctr\" ]; then "
        "for archive in \"$image_dir\"/*.tar; do "
        "[ -e \"$archive\" ] || continue; "
        "\"$ctr\" --address \"$socket\" -n k8s.io images import \"$archive\"; "
        "done; "
        "\"$ctr\" --address \"$socket\" -n k8s.io images ls >/dev/null; "
        "echo \"Imported preload archives into running RKE2 containerd.\"; "
        "else "
        "echo \"RKE2 containerd is not running on this node; archives are present for startup import.\"; "
        "fi"
    )
    run_command(["ssh", *ssh_options, target, f"sudo sh -lc {shlex.quote(script)}"])


def database_instances(values: dict[str, Any], namespace: str) -> list[dict[str, Any]]:
    instances = values.get("databases", {}).get("instances", {})
    targets: list[dict[str, Any]] = []
    for name, config in instances.items():
        if not isinstance(config, dict) or not config.get("enabled", False):
            continue
        engine = str(config.get("engine", "postgresql"))
        database = str(config.get("database", name))
        owner = str(config.get("owner", database))
        targets.append(
            {
                "name": str(name),
                "engine": engine,
                "host": f"{name}-rw.{namespace}.svc",
                "port": int(config.get("postgresPort", 5432)),
                "database": database,
                "owner": owner,
                "secretRef": {
                    "name": f"{name}-app",
                    "namespace": namespace,
                    "usernameKey": "username",
                    "passwordKey": "password",
                },
            }
        )
    return targets


def target_for_record(record: import_project.ServiceRecord, available: list[dict[str, Any]], index: int) -> dict[str, Any]:
    matching_index = next((i for i, target in enumerate(available) if target["engine"] == record.kind), None)
    if matching_index is None and record.kind == "postgresql":
        matching_index = next((i for i, target in enumerate(available) if target["engine"] in {"postgres", "postgresql"}), None)
    if matching_index is None and available:
        matching_index = 0
    if matching_index is None:
        return {
            "host": f"{k8s_name(record.name)}-rw.<namespace>.svc",
            "port": 5432,
            "database": "<target_database>",
            "username": "<target_user>",
            "password": "<target_password>",
        }
    return available.pop(matching_index)


def generated_database_targets(
    service_pairs: list[tuple[import_project.ServiceRecord, dict[str, Any]]],
    values: dict[str, Any],
    namespace: str,
) -> dict[str, Any]:
    available = database_instances(values, namespace)
    mapping: dict[str, Any] = {}
    database_records = [
        record
        for record, _service in service_pairs
        if record.kind in {"postgresql", "postgis", "timescaledb"}
    ]
    for index, record in enumerate(database_records):
        target = target_for_record(record, available, index)
        entry = {
            "sourceAlias": k8s_name(record.name),
            "targetService": target.get("name", "<target-service>"),
            "host": target["host"],
            "port": target["port"],
            "database": target["database"],
        }
        if target.get("secretRef"):
            entry["secretRef"] = target["secretRef"]
        if target.get("username"):
            entry["username"] = target["username"]
        if target.get("password"):
            entry["password"] = target["password"]
        mapping[record.name] = entry
    return {"databaseTargets": mapping}


def write_database_target_map(path: Path, service_pairs: list[tuple[import_project.ServiceRecord, dict[str, Any]]], values: dict[str, Any], namespace: str) -> None:
    should_replace_placeholder = False
    if path.exists():
        existing = path.read_text(encoding="utf-8")
        should_replace_placeholder = "service-name-or-alias" in existing
    if path.exists() and not should_replace_placeholder:
        secure_file(path)
        print(f"Database target map already exists: {path}")
        return
    write_file(path, yaml.safe_dump(generated_database_targets(service_pairs, values, namespace), sort_keys=False))
    secure_file(path)
    print(f"Database target map initialized: {path}")


def render_private_report(
    args: argparse.Namespace,
    private_report: Path,
    project_path: Path,
    compose_files: list[Path],
    service_pairs: list[tuple[import_project.ServiceRecord, dict[str, Any]]],
    findings: list[import_project.Finding],
    values: dict[str, Any],
) -> None:
    report = import_project.render_report(
        project_path=project_path,
        compose_files=compose_files,
        service_records=[record for record, _service in service_pairs],
        findings=findings,
        selected_ingress=args.ingress_controller,
        selected_webserver=args.webserver,
        selected_database=args.database,
        expected_web_image=import_project.expected_webserver_image(values, args.webserver),
        database_targets=import_project.database_target_images(values),
        redact_sensitive=False,
    )
    write_file(private_report, report)
    secure_file(private_report)
    print(f"Private import report written: {private_report}")


def print_bundle_files(output: Path) -> None:
    if not output.exists():
        return
    print("Migration bundle files:")
    for path in sorted(item for item in output.rglob("*") if item.is_file()):
        print(f"- {path.relative_to(output)}")


def stage_prepare(
    args: argparse.Namespace,
    project_path: Path,
    compose_files: list[Path],
    service_pairs: list[tuple[import_project.ServiceRecord, dict[str, Any]]],
    findings: list[import_project.Finding],
    values: dict[str, Any],
) -> None:
    private_dir = Path(args.private_dir).expanduser()
    secure_directory(private_dir)
    secure_directory(Path(args.dump_dir).expanduser())
    db_targets_path = Path(args.db_targets).expanduser() if args.db_targets else private_dir / "db-targets.yaml"
    write_database_target_map(db_targets_path, service_pairs, values, args.namespace)
    render_private_report(
        args=args,
        private_report=private_dir / "import-check-private.md",
        project_path=project_path,
        compose_files=compose_files,
        service_pairs=service_pairs,
        findings=findings,
        values=values,
    )
    ensure_registry_login(args)


