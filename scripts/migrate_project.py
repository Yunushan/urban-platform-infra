def kubernetes_secret_exists(args: argparse.Namespace, name: str) -> bool:
    command = kubectl_command(args, ["-n", args.namespace, "get", "secret", name, "--request-timeout=15s"])
    return subprocess.run(command, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL).returncode == 0


def kubernetes_service_exists(args: argparse.Namespace, name: str) -> bool:
    command = kubectl_command(args, ["-n", args.namespace, "get", "service", name, "--request-timeout=15s"])
    return subprocess.run(command, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL).returncode == 0


def ingress_backend_service_names(manifest: dict[str, Any]) -> list[str]:
    names: list[str] = []
    if manifest.get("kind") != "Ingress":
        return names

    default_backend = manifest.get("spec", {}).get("defaultBackend", {})
    default_service = default_backend.get("service", {})
    if isinstance(default_service, dict) and default_service.get("name"):
        names.append(str(default_service["name"]))

    for rule in manifest.get("spec", {}).get("rules", []):
        if not isinstance(rule, dict):
            continue
        http = rule.get("http", {})
        if not isinstance(http, dict):
            continue
        for path in http.get("paths", []):
            if not isinstance(path, dict):
                continue
            service = path.get("backend", {}).get("service", {})
            if isinstance(service, dict) and service.get("name"):
                names.append(str(service["name"]))
    return list(dict.fromkeys(names))


def executable_ingress_manifests(args: argparse.Namespace, manifests: list[dict[str, Any]]) -> list[dict[str, Any]]:
    executable: list[dict[str, Any]] = []
    for manifest in manifests:
        name = str(manifest.get("metadata", {}).get("name", "<unnamed>"))
        backend_services = ingress_backend_service_names(manifest)
        missing = [service for service in backend_services if not kubernetes_service_exists(args, service)]
        if missing:
            print(
                f"Skipping ingress candidate {name}: backend service(s) not found in namespace "
                f"{args.namespace}: {', '.join(missing)}. Deploy or create the backend Service before applying it."
            )
            continue
        executable.append(manifest)
    return executable


def ingress_san(host: str) -> str:
    try:
        ipaddress.ip_address(host)
    except ValueError:
        return f"DNS:{host}"
    return f"IP:{host}"


def apply_tls_secret_from_files(args: argparse.Namespace, name: str, cert_file: Path, key_file: Path) -> None:
    create_command = kubectl_command(
        args,
        [
            "-n",
            args.namespace,
            "create",
            "secret",
            "tls",
            name,
            "--cert",
            str(cert_file),
            "--key",
            str(key_file),
            "--dry-run=client",
            "-o",
            "yaml",
        ],
    )
    result = subprocess.run(create_command, text=True, capture_output=True, check=True)
    run_command(kubectl_apply_stdin_command(args, namespace=args.namespace), stdin=result.stdout)


def ensure_ingress_tls_secret(args: argparse.Namespace, values: dict[str, Any], host: str) -> None:
    tls_values = values.get("ingress", {}).get("tls", {})
    if not tls_values.get("enabled", True):
        return
    secret_name = ingress_tls_secret_name(values)
    if kubernetes_secret_exists(args, secret_name):
        print(f"Ingress TLS secret already exists: {secret_name}")
        return

    cert_file_arg = args.tls_cert_file
    key_file_arg = args.tls_key_file
    if cert_file_arg or key_file_arg:
        if not cert_file_arg or not key_file_arg:
            raise SystemExit("Both MIGRATION_TLS_CERT_FILE and MIGRATION_TLS_KEY_FILE are required when providing an ingress certificate.")
        cert_file = Path(cert_file_arg).expanduser()
        key_file = Path(key_file_arg).expanduser()
        if not cert_file.is_file() or not key_file.is_file():
            raise SystemExit(f"Provided ingress TLS files were not found: cert={cert_file} key={key_file}")
        print(f"Creating ingress TLS secret {secret_name} from provided certificate files.")
        apply_tls_secret_from_files(args, secret_name, cert_file, key_file)
        return

    if not host:
        raise SystemExit("Cannot create a self-signed ingress certificate without MIGRATION_INGRESS_HOST, MIGRATION_CLUSTER_DOMAIN, ingress.host, or global.cluster.domain.")
    if not shutil.which("openssl"):
        raise SystemExit("Cannot create default self-signed ingress certificate because `openssl` is not installed on the operator machine.")

    duration_days = int(tls_values.get("selfSigned", {}).get("durationDays", 3650))
    with tempfile.TemporaryDirectory(prefix="urban-platform-tls-") as tmp_dir:
        cert_file = Path(tmp_dir) / "tls.crt"
        key_file = Path(tmp_dir) / "tls.key"
        run_command(
            [
                "openssl",
                "req",
                "-x509",
                "-nodes",
                "-newkey",
                "rsa:2048",
                "-days",
                str(duration_days),
                "-subj",
                f"/CN={host}",
                "-addext",
                f"subjectAltName={ingress_san(host)}",
                "-keyout",
                str(key_file),
                "-out",
                str(cert_file),
            ]
        )
        print(f"Creating self-signed ingress TLS secret {secret_name} for {host}.")
        apply_tls_secret_from_files(args, secret_name, cert_file, key_file)


def stage_manifests(args: argparse.Namespace, service_pairs: list[tuple[import_project.ServiceRecord, dict[str, Any]]], values: dict[str, Any]) -> None:
    output = Path(args.output).expanduser()
    scoped_service_pairs = filter_service_pairs_for_import_batch(args, service_pairs)
    plan = import_batch_plan(args, service_pairs)
    if plan["selectedBatch"] is not None:
        print(f"Import batch {plan['selectedBatch']}/{plan['totalBatches']} selected for workload manifests.")
    workload_manifests = kubernetes_workload_manifests(args, scoped_service_pairs)
    ingress_manifests: list[dict[str, Any]] = []
    host = ingress_host(args, values)
    rule_host = ingress_rule_host(host)
    if host and not rule_host:
        print(f"Ingress host `{host}` is an IP address; generating hostless Traefik Ingress rules.")
    tls_enabled = bool(values.get("ingress", {}).get("tls", {}).get("enabled", True))
    tls_secret_name = ingress_tls_secret_name(values)
    for record, service in scoped_service_pairs:
        if record.kind == "nginx" and import_project.has_edge_publish(record.ports):
            name = kubernetes_workload_name(record)
            rule: dict[str, Any] = {
                "http": {
                    "paths": [
                        {
                            "path": "/",
                            "pathType": "Prefix",
                            "backend": {
                                "service": {"name": name, "port": {"number": 80}}
                            },
                        }
                    ]
                }
            }
            if rule_host:
                rule["host"] = rule_host
            spec: dict[str, Any] = {
                "ingressClassName": "traefik",
                "rules": [rule],
            }
            annotations = {"traefik.ingress.kubernetes.io/router.entrypoints": "websecure"}
            if ingress_source_allowlist_cidrs(values):
                annotations["traefik.ingress.kubernetes.io/router.middlewares"] = traefik_source_allowlist_middleware_ref(args)
            if tls_enabled:
                tls_entry: dict[str, Any] = {"secretName": tls_secret_name}
                if rule_host:
                    tls_entry["hosts"] = [rule_host]
                spec["tls"] = [tls_entry]
                annotations["traefik.ingress.kubernetes.io/router.tls"] = "true"
            ingress_manifests.append(
                {
                    "apiVersion": "networking.k8s.io/v1",
                    "kind": "Ingress",
                    "metadata": {
                        "name": f"{name}-traefik",
                        "namespace": args.namespace,
                        "annotations": annotations,
                    },
                    "spec": spec,
                }
            )
    workloads_content = GENERATED_HEADER + yaml.safe_dump_all(workload_manifests, sort_keys=False)
    workloads_path = output / "manifests" / "imported-workloads.yaml"
    write_file(workloads_path, workloads_content)
    print(f"Wrote {workloads_path}")
    ingress_content = GENERATED_HEADER + yaml.safe_dump_all(ingress_manifests, sort_keys=False)
    ingress_path = output / "manifests" / "traefik-ingress-candidates.yaml"
    write_file(ingress_path, ingress_content)
    print(f"Wrote {ingress_path}")
    if args.execute and workload_manifests:
        run_command(
            kubectl_apply_stdin_command(args, namespace=args.namespace),
            stdin=yaml.safe_dump_all(workload_manifests, sort_keys=False),
        )
    if args.execute and ingress_manifests:
        ensure_ingress_tls_secret(args, values, host)
        executable_manifests = executable_ingress_manifests(args, ingress_manifests)
        if executable_manifests:
            run_command(
                kubectl_apply_stdin_command(args, namespace=args.namespace),
                stdin=yaml.safe_dump_all(executable_manifests, sort_keys=False),
            )
        else:
            print("No ingress candidates were applied because their backend services are not present yet.")


