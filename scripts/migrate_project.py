def ingress_source_allowlist_cidrs(values: dict[str, Any]) -> list[str]:
    source_allowlist = values.get("ingress", {}).get("sourceAllowList", {})
    if not isinstance(source_allowlist, dict) or not source_allowlist.get("enabled", False):
        return []
    cidrs = [str(item).strip() for item in source_allowlist.get("cidrs", []) if str(item).strip()]
    cidrs_text = str(source_allowlist.get("cidrsText", "")).strip()
    if cidrs_text:
        cidrs.extend(item for item in cidrs_text.split() if item)
    return list(dict.fromkeys(cidrs))


