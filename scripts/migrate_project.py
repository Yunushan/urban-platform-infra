def load_context(args: argparse.Namespace) -> tuple[Path, list[Path], list[tuple[import_project.ServiceRecord, dict[str, Any]]], list[import_project.Finding], dict[str, Any]]:
    require_yaml()
    project_path = Path(args.project_path).expanduser().resolve()
    if not project_path.exists() or not project_path.is_dir():
        raise SystemExit(f"Project path does not exist or is not a directory: {project_path}")

    values_path = Path(args.values).expanduser()
    if not values_path.is_absolute():
        values_path = (ROOT / values_path).resolve()
    policy_path = Path(args.image_policy).expanduser()
    if not policy_path.is_absolute():
        policy_path = (ROOT / policy_path).resolve()

    values = import_project.load_yaml(values_path)
    approved_images = import_project.normalize_approved_images(policy_path)
    selected_ingress = args.ingress_controller or values.get("ingress", {}).get("className", "traefik")
    selected_webserver = args.webserver or values.get("webserver", {}).get("provider", "nginx")
    selected_database = args.database or values.get("databases", {}).get("provider", "cloudnative-pg")
    expected_web_image = import_project.expected_webserver_image(values, selected_webserver)

    findings: list[import_project.Finding] = []
    compose_files = import_project.find_compose_files(project_path)
    service_pairs = import_project.read_compose_services(project_path, compose_files, findings)
    for record, service in service_pairs:
        import_project.analyze_service(
            service=service,
            record=record,
            findings=findings,
            approved_images=approved_images,
            selected_ingress=str(selected_ingress),
            selected_webserver=str(selected_webserver),
            selected_database=str(selected_database),
            expected_web_image=expected_web_image,
        )
    return project_path, compose_files, service_pairs, findings, values


def secret_entries(service: dict[str, Any]) -> dict[str, str]:
    entries: dict[str, str] = {}
    for key, value in import_project.environment_entries(service.get("environment")):
        if not SECRET_ENV_RE.search(key):
            continue
        if value is None or import_project.VARIABLE_VALUE_RE.match(value):
            continue
        entries[key] = value
    return entries


def postgres_env(service: dict[str, Any]) -> dict[str, str]:
    env = {key: value for key, value in import_project.environment_entries(service.get("environment")) if value is not None}
    return {
        "user": env.get("POSTGRES_USER", "postgres"),
        "password": env.get("POSTGRES_PASSWORD", ""),
        "database": env.get("POSTGRES_DB", env.get("POSTGRES_USER", "postgres")),
    }


def published_port(record: import_project.ServiceRecord, target: str = "5432") -> str | None:
    for port in record.ports:
        if port.target == target and port.published:
            return port.published
    return None


def read_database_targets(path: str | None) -> dict[str, Any]:
    if not path:
        return {}
    target_path = Path(path).expanduser()
    if not target_path.exists():
        raise SystemExit(f"Database target map does not exist: {target_path}")
    data = yaml.safe_load(target_path.read_text(encoding="utf-8")) or {}
    targets = data.get("databaseTargets", data)
    if not isinstance(targets, dict):
        raise SystemExit("Database target map must be a mapping or contain `databaseTargets`.")
    return {str(key): value for key, value in targets.items()}


def k8s_secret_data(namespace: str, name: str) -> dict[str, str]:
    result = subprocess.run(
        ["kubectl", "-n", namespace, "get", "secret", name, "-o", "json"],
        text=True,
        capture_output=True,
        check=True,
    )
    payload = json.loads(result.stdout)
    encoded_data = payload.get("data", {})
    return {
        key: base64.b64decode(value).decode("utf-8")
        for key, value in encoded_data.items()
    }


def target_dsn_from_mapping(target: Any, namespace: str) -> str | None:
    if isinstance(target, str):
        if any(marker in target for marker in ["<target_", "<namespace>"]):
            return None
        return target
    if not isinstance(target, dict):
        return None

    host = target.get("host")
    database = target.get("database")
    port = target.get("port", 5432)
    username = target.get("username")
    password = target.get("password")
    secret_ref = target.get("secretRef") or target.get("secret")
    if isinstance(secret_ref, dict) and (not username or not password):
        secret_namespace = str(secret_ref.get("namespace") or namespace)
        secret_name = str(secret_ref.get("name"))
        try:
            secret_data = k8s_secret_data(secret_namespace, secret_name)
        except subprocess.CalledProcessError:
            print(f"Could not read target database secret {secret_namespace}/{secret_name}; restore for this target will be skipped.")
            return None
        username = secret_data.get(str(secret_ref.get("usernameKey", "username")))
        password = password or secret_data.get(str(secret_ref.get("passwordKey", "password")))
        database = database or secret_data.get(str(secret_ref.get("databaseKey", "dbname")))
        host = host or secret_data.get(str(secret_ref.get("hostKey", "host")))
        port = target.get("port") or secret_data.get(str(secret_ref.get("portKey", "port")), 5432)
    if not host or not database or not username or not password:
        return None
    if str(password).startswith("<") or str(username).startswith("<") or str(database).startswith("<"):
        return None
    return f"postgresql://{quote(str(username), safe='')}:{quote(str(password), safe='')}@{host}:{port}/{quote(str(database), safe='')}"


def run_command(command: list[str], *, env: dict[str, str] | None = None, stdin: str | None = None, cwd: Path | None = None) -> None:
    display = " ".join(command)
    print(f"+ {display}")
    subprocess.run(command, input=stdin, text=True, env=env, cwd=str(cwd) if cwd else None, check=True)


def write_file(path: Path, content: str, executable: bool = False) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    if executable:
        path.chmod(path.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP)


def secure_directory(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)
    try:
        path.chmod(0o700)
    except PermissionError:
        print(f"Could not chmod {path}; verify permissions manually.")


def secure_file(path: Path) -> None:
    try:
        path.chmod(0o600)
    except PermissionError:
        print(f"Could not chmod {path}; verify permissions manually.")


def registry_host(registry: str) -> str:
    return registry.split("/", 1)[0]


def ensure_registry_login(args: argparse.Namespace) -> None:
    if not args.execute or args.image_mode != "registry" or not args.registry:
        return
    username = os.environ.get("MIGRATION_REGISTRY_USERNAME")
    password = os.environ.get("MIGRATION_REGISTRY_PASSWORD")
    if not username or not password:
        print("Registry credentials were not provided in MIGRATION_REGISTRY_USERNAME/MIGRATION_REGISTRY_PASSWORD; using existing Docker login state.")
        return
    run_command(["docker", "login", registry_host(args.registry), "--username", username, "--password-stdin"], stdin=password)


