def imported_secret_name(record: import_project.ServiceRecord) -> str:
    return k8s_name(record.name, "import-env")


def secret_remote_key(args: argparse.Namespace, secret_name: str) -> str:
    prefix = str(args.secret_remote_prefix or "").strip("/")
    return f"{prefix}/{secret_name}" if prefix else secret_name


def kubernetes_secret_manifest(args: argparse.Namespace, record: import_project.ServiceRecord, entries: dict[str, str]) -> dict[str, Any]:
    return {
        "apiVersion": "v1",
        "kind": "Secret",
        "metadata": {
            "name": imported_secret_name(record),
            "namespace": args.namespace,
            "labels": {"app.kubernetes.io/managed-by": "urban-platform-import"},
        },
        "type": "Opaque",
        "stringData": entries,
    }


def external_secret_manifest(args: argparse.Namespace, record: import_project.ServiceRecord, entries: dict[str, str]) -> dict[str, Any]:
    secret_name = imported_secret_name(record)
    remote_key = secret_remote_key(args, secret_name)
    return {
        "apiVersion": "external-secrets.io/v1",
        "kind": "ExternalSecret",
        "metadata": {
            "name": secret_name,
            "namespace": args.namespace,
            "labels": {
                "app.kubernetes.io/managed-by": "urban-platform-import",
                "urban-platform.io/secret-delivery": args.secret_provider,
            },
        },
        "spec": {
            "refreshInterval": args.secret_refresh_interval,
            "secretStoreRef": {
                "name": args.secret_store_name,
                "kind": args.secret_store_kind,
            },
            "target": {
                "name": secret_name,
                "creationPolicy": "Owner",
                "template": {"type": "Opaque"},
            },
            "data": [
                {"secretKey": key, "remoteRef": {"key": remote_key, "property": key}}
                for key in sorted(entries)
            ],
        },
    }


def write_secret_provider_report(
    args: argparse.Namespace,
    secrets: list[tuple[import_project.ServiceRecord, dict[str, str]]],
) -> None:
    output = Path(args.output).expanduser()
    provider = args.secret_provider
    remote_prefix = "<remote-prefix>" if args.redact_sensitive and args.secret_remote_prefix else (args.secret_remote_prefix or "-")
    automatic = provider in AUTOMATIC_SECRET_PROVIDERS
    lines = [
        "# Import Secret Provider",
        "",
        "This report is public-safe. It summarizes the selected secret delivery adapter without printing secret values.",
        "",
        f"- Provider: `{provider}`",
        f"- Secret-bearing services: `{len(secrets)}`",
        f"- Automatic apply provider: `{str(automatic).lower()}`",
        f"- Remote prefix: `{remote_prefix}`",
        f"- SecretStore name: `{'<secret-store>' if args.redact_sensitive else args.secret_store_name}`",
        f"- SecretStore kind: `{args.secret_store_kind}`",
        "",
        "## Behavior",
        "",
    ]
    if provider == "kubernetes":
        lines.append("- Direct Kubernetes Secret import is used only when `MIGRATION_ALLOW_SECRET_MATERIAL=true` is set.")
    elif provider in {"external-secrets", "vault"}:
        lines.append("- The migration stage applies `ExternalSecret` resources and does not place secret values in generated manifests.")
        lines.append("- The remote provider must already contain the referenced keys before workloads depend on them.")
    else:
        lines.append("- This provider is an encrypted handoff mode. The migration stage does not apply plain Kubernetes Secret objects.")
        lines.append("- Generate final encrypted artifacts with the selected SOPS or Sealed Secrets workflow before deployment.")
    write_file(output / "import-secret-provider.md", "\n".join(lines) + "\n")


def stage_secrets(args: argparse.Namespace, service_pairs: list[tuple[import_project.ServiceRecord, dict[str, Any]]]) -> None:
    scoped_service_pairs = filter_service_pairs_for_import_batch(args, service_pairs)
    plan = import_batch_plan(args, service_pairs)
    if plan["selectedBatch"] is not None:
        print(f"Import batch {plan['selectedBatch']}/{plan['totalBatches']} selected for service-specific secrets.")
    secrets = []
    for record, service in scoped_service_pairs:
        entries = secret_entries(service)
        if entries:
            secrets.append((record, entries))
    print(f"Secret-bearing services: {len(secrets)}")
    write_secret_provider_report(args, secrets)
    if not args.execute:
        return
    if args.secret_provider not in SECRET_PROVIDER_CHOICES:
        raise SystemExit(f"Unsupported secret provider: {args.secret_provider}")
    if args.secret_provider not in AUTOMATIC_SECRET_PROVIDERS:
        raise SystemExit(
            f"MIGRATION_SECRET_PROVIDER={args.secret_provider} is an encrypted handoff mode. "
            "Use external-secrets or vault for automatic runtime sync, or kubernetes with explicit secret material approval."
        )
    if args.secret_provider == "kubernetes" and not args.allow_secret_material:
        raise SystemExit("Refusing to read/apply secret material without --allow-secret-material.")
    for record, entries in secrets:
        if args.secret_provider == "kubernetes":
            manifest = kubernetes_secret_manifest(args, record, entries)
        else:
            manifest = external_secret_manifest(args, record, entries)
        run_command(
            kubectl_apply_stdin_command(args, namespace=args.namespace),
            stdin=yaml.safe_dump(manifest, sort_keys=False),
        )


def stage_images(args: argparse.Namespace, project_path: Path, service_pairs: list[tuple[import_project.ServiceRecord, dict[str, Any]]]) -> None:
    scoped_service_pairs = filter_service_pairs_for_import_batch(args, service_pairs)
    plan = import_batch_plan(args, service_pairs)
    if plan["selectedBatch"] is not None:
        print(f"Import batch {plan['selectedBatch']}/{plan['totalBatches']} selected for image migration.")
    candidates = []
    for record, service in scoped_service_pairs:
        if args.skip_docker_socket_services and service_has_docker_socket(service):
            print(f"Skipping Docker-socket service image candidate: {record.file}::{record.name}")
            continue
        if record.kind != "application" and not record.build_only:
            continue
        image = record.image
        build = service.get("build")
        needs_tag = record.build_only or (image is not None and (not image.tag or image.tag in import_project.MUTABLE_TAGS))
        if needs_tag:
            candidates.append((record, service, build))
    source_image_records: dict[str, list[import_project.ServiceRecord]] = {}
    for record, _service, _build in candidates:
        if record.image is not None:
            source_image_records.setdefault(record.image.display, []).append(record)
    source_preloaded_refs: dict[str, list[str]] = {}
    print(f"Application image promotion candidates: {len(candidates)}")
    if not args.execute:
        return
    if args.image_mode == "skip":
        print("Image migration skipped by MIGRATION_IMAGE_MODE=skip. Keep these services on Compose or provide images another way.")
        return
    if args.image_mode == "registry" and not args.registry:
        raise SystemExit("Refusing image promotion without --registry or MIGRATION_REGISTRY.")
    print(f"Using container CLI for image migration: {container_tool(args)}")
    ensure_registry_login(args)
    archive_dir = Path(args.image_output_dir).expanduser()
    if args.image_mode == "preload":
        archive_dir.mkdir(parents=True, exist_ok=True)
        print("Preload mode stages each image on the operator, then streams it to the RKE2 nodes.")
    skipped: list[str] = []
    failed: list[str] = []
    generated_images: list[str] = []
    pulled_source_images: list[str] = []
    generated_archives: list[Path] = []
    preload_streaming = args.image_mode == "preload" and bool([node for node in args.rke2_nodes.split(",") if node.strip()])
    preload_initialized = False
    archives_copied = False
    stale_archive_names = {
        archive_name
        for record, _service, _build in candidates
        for archive_name in (image_archive_name(record), legacy_image_archive_name(record))
    }
    for record, service, build in candidates:
        target_image = local_import_image(args, record)
        image_refs = containerd_import_image_refs(target_image)
        compose_path = relative_compose_file(project_path, record.file)
        archive_path = archive_dir / image_archive_name(record) if args.image_mode == "preload" else None
        retry_build: tuple[Path, str] | None = None
        try:
            if preload_streaming and all_preload_nodes_have_image(args, image_refs):
                print(f"Image {image_refs[0]} is already present on all RKE2 nodes; skipping build, archive save, and upload.")
                if record.image is not None:
                    source_preloaded_refs.setdefault(record.image.display, image_refs)
                if args.cleanup_operator_images:
                    cleanup_operator_container_tags(args, [target_image])
                continue
            if build:
                context, dockerfile, fallback_note = resolve_build_source(compose_path, build)
                if fallback_note:
                    print(fallback_note)
                if not context.exists() or not context.is_dir():
                    skipped.append(f"{record.file}::{record.name} (build context does not exist: {context})")
                    continue
                dockerfile_path = (context / dockerfile).resolve()
                if not dockerfile_path.exists() or not dockerfile_path.is_file():
                    skipped.append(f"{record.file}::{record.name} (Dockerfile does not exist: {dockerfile_path})")
                    continue
                retry_build = (context, dockerfile)
                run_command(container_command(args, "build", "-t", target_image, "-f", dockerfile, "."), cwd=context)
            elif record.image:
                source_ready, source_cleanup_images = ensure_source_image(args, record)
                if not source_ready:
                    reused_source_refs = source_preloaded_refs.get(record.image.display, [])
                    if not reused_source_refs and preload_streaming:
                        for sibling_record in source_image_records.get(record.image.display, []):
                            if sibling_record is record:
                                continue
                            sibling_refs = containerd_import_image_refs(local_import_image(args, sibling_record))
                            if all_preload_nodes_have_image(args, sibling_refs):
                                reused_source_refs = sibling_refs
                                source_preloaded_refs.setdefault(record.image.display, sibling_refs)
                                break
                    if reused_source_refs and preload_streaming and tag_preloaded_image_on_nodes(args, reused_source_refs, image_refs):
                        print(
                            f"Source image {record.image.display} is unavailable; "
                            f"tagged existing preloaded sibling image as {image_refs[0]} on all RKE2 nodes."
                        )
                        source_preloaded_refs.setdefault(record.image.display, image_refs)
                        continue
                    if archive_path is not None and archive_path.exists():
                        print(
                            f"Source image {record.image.display} is unavailable; "
                            f"reusing existing preload archive {archive_path}."
                        )
                        if preload_streaming:
                            copied = preload_archives_to_nodes(
                                args,
                                [archive_path],
                                stale_archive_names if not preload_initialized else set(),
                                image_refs,
                            )
                            archives_copied = archives_copied or copied
                            preload_initialized = True
                            if copied and args.cleanup_operator_images and archive_path.exists():
                                archive_path.unlink()
                            source_preloaded_refs.setdefault(record.image.display, image_refs)
                        else:
                            generated_archives.append(archive_path)
                        continue
                    skipped.append(f"{record.file}::{record.name} ({record.image.display})")
                    continue
                pulled_source_images.extend(source_cleanup_images)
                run_command(container_command(args, "tag", record.image.display, target_image))
            generated_images.append(target_image)
            if args.image_mode == "registry":
                run_command(container_command(args, "push", target_image))
            elif args.image_mode == "preload":
                assert archive_path is not None
                if preload_streaming and all_preload_nodes_have_image(args, image_refs):
                    print(f"Image {image_refs[0]} is already present on all RKE2 nodes; skipping local archive save and upload.")
                    if archive_path.exists():
                        archive_path.unlink()
                    if record.image is not None:
                        source_preloaded_refs.setdefault(record.image.display, image_refs)
                    if args.cleanup_operator_images:
                        cleanup_operator_container_tags(args, [target_image])
                    continue
                save_image_archive(args, archive_path, target_image, retry_build)
                if preload_streaming:
                    copied = preload_archives_to_nodes(
                        args,
                        [archive_path],
                        stale_archive_names if not preload_initialized else set(),
                        image_refs,
                    )
                    archives_copied = archives_copied or copied
                    preload_initialized = True
                    if copied and args.cleanup_operator_images and archive_path.exists():
                        archive_path.unlink()
                    if copied and record.image is not None:
                        source_preloaded_refs.setdefault(record.image.display, image_refs)
                else:
                    generated_archives.append(archive_path)
            if args.cleanup_operator_images:
                cleanup_operator_container_tags(args, [target_image])
        except subprocess.CalledProcessError as exc:
            failed.append(f"{record.file}::{record.name} ({exc.cmd})")
    if args.image_mode == "preload" and not preload_streaming:
        archives_copied = preload_archives_to_nodes(args, generated_archives, stale_archive_names)
        if args.cleanup_operator_images:
            cleanup_operator_archives(archive_dir)
    if args.cleanup_operator_images:
        cleanup_operator_container_tags(args, generated_images + pulled_source_images)
        prune_operator_container_cache(args)
    if skipped:
        print("Skipped image candidates:")
        for item in skipped:
            print(f"- {item}")
        raise SystemExit(
            "Image migration skipped required selected candidate(s). "
            "Stopping before workload rollout so Kubernetes is not left in ImagePullBackOff. "
            "Make the source image available on the operator, keep the Compose build context present, "
            "or rerun intentionally with MIGRATION_IMAGE_MODE=skip for a routing-only transition."
        )
    if failed:
        print("Failed image candidates:")
        for item in failed:
            print(f"- {item}")
        raise SystemExit(f"Image migration completed with {len(failed)} failed candidate(s).")


def stage_databases(args: argparse.Namespace, service_pairs: list[tuple[import_project.ServiceRecord, dict[str, Any]]]) -> None:
    db_targets_path = str(database_targets_path(args))
    targets = read_database_targets(db_targets_path)
    dump_dir = Path(args.dump_dir).expanduser()
    candidates = [
        (record, service)
        for record, service in service_pairs
        if record.kind in POSTGRES_FAMILY_KINDS
    ]
    optional_candidates = [
        record
        for record, _service in service_pairs
        if record.kind in OPTIONAL_DATABASE_KINDS
    ]
    print(f"PostgreSQL-family migration candidates: {len(candidates)}")
    print(f"Optional database target scaffolds: {len(optional_candidates)}")
    if optional_candidates:
        print(
            "Optional database engines are scaffolded in the private target map; "
            "engine-specific automated dump/restore is not enabled until a matching operator, "
            "managed service, or external target profile is declared."
        )
    if not args.execute:
        return
    if not args.allow_secret_material:
        raise SystemExit("Refusing database dump/restore without --allow-secret-material.")
    dump_dir.mkdir(parents=True, exist_ok=True)
    skipped_sources: list[str] = []
    reachable_candidates: list[tuple[import_project.ServiceRecord, dict[str, Any], str, dict[str, str], str, Path]] = []
    for record, service in candidates:
        port = published_port(record)
        env = postgres_env(service)
        if not port or not env["password"]:
            print(f"Skipping {record.file}::{record.name}: missing published port or POSTGRES_PASSWORD.")
            continue
        alias = k8s_name(record.name)
        dump_file = dump_dir / f"{alias}.dump"
        if args.skip_unavailable_databases and not tcp_endpoint_available("127.0.0.1", port):
            message = f"{record.file}::{record.name} source database at 127.0.0.1:{port} is not reachable from the operator."
            print(f"Skipping database source: {message}")
            skipped_sources.append(message)
            if dump_file.exists():
                dump_file.unlink()
            continue
        reachable_candidates.append((record, service, port, env, alias, dump_file))
    if not reachable_candidates:
        if skipped_sources:
            print("Skipped unavailable PostgreSQL-family source database(s):")
            for item in skipped_sources:
                print(f"- {item}")
        return
    use_local_pg_tools = local_postgres_client_tools_available()
    if use_local_pg_tools:
        print("Using local pg_dump/pg_restore for PostgreSQL-family migration.")
    else:
        print(f"Local pg_dump/pg_restore not found; using containerized PostgreSQL client image {args.postgres_client_image}.")
        ensure_container_image(args, args.postgres_client_image, "PostgreSQL client")
    for record, service, port, env, alias, dump_file in reachable_candidates:
        run_env = os.environ.copy()
        run_env["PGPASSWORD"] = env["password"]
        dump_args = [
            "--format=custom",
            "--no-owner",
            "--no-acl",
            "--host",
            "127.0.0.1",
            "--port",
            port,
            "--username",
            env["user"],
            "--dbname",
            env["database"],
            "--file",
            str(dump_file),
        ]
        if use_local_pg_tools:
            dump_command = ["pg_dump", *dump_args]
        else:
            container_dump_args = [
                *dump_args[:-1],
                f"/urban-platform-db-dumps/{dump_file.name}",
            ]
            dump_command = postgres_client_container_command(args, dump_dir, ["PGPASSWORD"], "pg_dump", container_dump_args)
        try:
            run_command(dump_command, env=run_env)
        except subprocess.CalledProcessError as exc:
            message = (
                f"{record.file}::{record.name} source database at 127.0.0.1:{port} "
                f"could not be dumped with user {env['user']} and database {env['database']}."
            )
            if args.skip_unavailable_databases:
                print(f"Skipping database source: {message}")
                skipped_sources.append(message)
                if dump_file.exists():
                    dump_file.unlink()
                continue
            raise SystemExit(f"Database dump failed: {message}") from exc
        target_dsn = target_dsn_from_mapping(targets.get(record.name) or targets.get(alias), args.namespace, args.kubeconfig)
        if target_dsn:
            restore_args = [
                "--clean",
                "--if-exists",
                "--no-owner",
                "--dbname",
                target_dsn,
                str(dump_file),
            ]
            if use_local_pg_tools:
                run_command(["pg_restore", *restore_args], env=os.environ.copy())
            else:
                container_restore_args = [
                    *restore_args[:-1],
                    f"/urban-platform-db-dumps/{dump_file.name}",
                ]
                run_command(
                    postgres_client_container_command(args, dump_dir, [], "pg_restore", container_restore_args),
                    env=os.environ.copy(),
                )
        else:
            print(f"Dumped {alias}; no usable target mapping found in {db_targets_path}, so restore was skipped.")
    if skipped_sources:
        print("Skipped unavailable PostgreSQL-family source database(s):")
        for item in skipped_sources:
            print(f"- {item}")


