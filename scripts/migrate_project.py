def command_details(result: subprocess.CompletedProcess[str]) -> str:
    return "\n".join(part for part in [result.stdout.strip(), result.stderr.strip()] if part)


def kubectl_capture(args: argparse.Namespace, command: list[str]) -> subprocess.CompletedProcess[str]:
    return subprocess.run(kubectl_command(args, command), text=True, capture_output=True)


def kubectl_json(args: argparse.Namespace, command: list[str]) -> dict[str, Any]:
    result = kubectl_capture(args, [*command, "-o", "json", "--request-timeout=20s"])
    if result.returncode != 0:
        raise RuntimeError(command_details(result))
    try:
        return json.loads(result.stdout)
    except json.JSONDecodeError as exc:
        raise RuntimeError(f"kubectl returned invalid JSON for {' '.join(command)}: {exc}") from exc


def require_kubernetes_api(args: argparse.Namespace) -> None:
    if not args.execute:
        return
    result = kubectl_capture(args, ["get", "--raw=/readyz", "--request-timeout=10s"])
    if result.returncode == 0:
        ensure_kubernetes_namespace(args)
        return
    details = command_details(result)
    kubeconfig_note = f" with kubeconfig {args.kubeconfig}" if args.kubeconfig else ""
    raise SystemExit(
        "Kubernetes API is not reachable"
        f"{kubeconfig_note}. import-auto cannot apply secrets, read target database secrets, or apply manifests until this is fixed.\n"
        "The Makefile now runs `make operator-kubeconfig` before import-auto; if this still fails, verify the RKE2 API/VIP listener, firewall rules, and cluster health.\n"
        f"kubectl output:\n{details}"
    )


def ensure_kubernetes_namespace(args: argparse.Namespace) -> None:
    get_command = kubectl_command(args, ["get", "namespace", args.namespace, "--request-timeout=15s"])
    create_command = kubectl_command(args, ["create", "namespace", args.namespace, "--request-timeout=30s"])

    last_details = ""
    for attempt in range(1, KUBERNETES_NAMESPACE_RETRIES + 1):
        result = subprocess.run(get_command, text=True, capture_output=True)
        if result.returncode == 0:
            return
        last_details = "\n".join(part for part in [result.stdout.strip(), result.stderr.strip()] if part)
        if "notfound" in last_details.replace(" ", "").lower() or "not found" in last_details.lower():
            break
        if attempt == KUBERNETES_NAMESPACE_RETRIES:
            raise SystemExit(
                f"Could not verify Kubernetes namespace `{args.namespace}` after {KUBERNETES_NAMESPACE_RETRIES} attempts.\n"
                "The Kubernetes API answered version checks, but namespace reads are timing out or failing.\n"
                f"kubectl output:\n{last_details}"
            )
        print(
            f"Kubernetes namespace check failed on attempt {attempt}/{KUBERNETES_NAMESPACE_RETRIES}; "
            f"retrying in {KUBERNETES_NAMESPACE_RETRY_DELAY_SECONDS}s."
        )
        time.sleep(KUBERNETES_NAMESPACE_RETRY_DELAY_SECONDS)

    for attempt in range(1, KUBERNETES_NAMESPACE_RETRIES + 1):
        run_display = " ".join(create_command)
        print(f"+ {run_display}")
        result = subprocess.run(create_command, text=True, capture_output=True)
        if result.returncode == 0:
            print(result.stdout, end="")
            return

        last_details = "\n".join(part for part in [result.stdout.strip(), result.stderr.strip()] if part)
        if "alreadyexists" in last_details.replace(" ", "").lower() or "already exists" in last_details.lower():
            return

        verify = subprocess.run(get_command, text=True, capture_output=True)
        if verify.returncode == 0:
            print(f"Kubernetes namespace `{args.namespace}` exists after create retry.")
            return

        if attempt == KUBERNETES_NAMESPACE_RETRIES:
            raise SystemExit(
                f"Could not create Kubernetes namespace `{args.namespace}` after {KUBERNETES_NAMESPACE_RETRIES} attempts.\n"
                "The cluster may be unhealthy or the API endpoint may be too slow for writes.\n"
                f"kubectl output:\n{last_details}"
            )
        print(
            f"Kubernetes namespace create failed on attempt {attempt}/{KUBERNETES_NAMESPACE_RETRIES}; "
            f"retrying in {KUBERNETES_NAMESPACE_RETRY_DELAY_SECONDS}s."
        )
        time.sleep(KUBERNETES_NAMESPACE_RETRY_DELAY_SECONDS)


def k8s_secret_data(namespace: str, name: str, kubeconfig: str = "") -> dict[str, str]:
    command = ["kubectl"]
    if kubeconfig:
        command.extend(["--kubeconfig", kubeconfig])
    command.extend(["-n", namespace, "get", "secret", name, "-o", "json"])
    result = subprocess.run(
        command,
        text=True,
        capture_output=True,
        check=True,
    )
    payload = json.loads(result.stdout)
    encoded_data = payload.get("data", {})
    return {
        key: base64.b64decode(value).decode("utf-8")
        for key, value in encoded_data.items()
    }


def target_dsn_from_mapping(target: Any, namespace: str, kubeconfig: str = "") -> str | None:
    if isinstance(target, str):
        if any(marker in target for marker in ["<target_", "<namespace>"]):
            return None
        return target
    if not isinstance(target, dict):
        return None

    host = target.get("host")
    database = target.get("database")
    port = target.get("port", 5432)
    username = target.get("username")
    password = target.get("password")
    secret_ref = target.get("secretRef") or target.get("secret")
    if isinstance(secret_ref, dict) and (not username or not password):
        secret_namespace = str(secret_ref.get("namespace") or namespace)
        secret_name = str(secret_ref.get("name"))
        try:
            secret_data = k8s_secret_data(secret_namespace, secret_name, kubeconfig)
        except subprocess.CalledProcessError:
            print(f"Could not read target database secret {secret_namespace}/{secret_name}; restore for this target will be skipped.")
            return None
        username = secret_data.get(str(secret_ref.get("usernameKey", "username")))
        password = password or secret_data.get(str(secret_ref.get("passwordKey", "password")))
        database = database or secret_data.get(str(secret_ref.get("databaseKey", "dbname")))
        host = host or secret_data.get(str(secret_ref.get("hostKey", "host")))
        port = target.get("port") or secret_data.get(str(secret_ref.get("portKey", "port")), 5432)
    if not host or not database or not username or not password:
        return None
    if str(password).startswith("<") or str(username).startswith("<") or str(database).startswith("<"):
        return None
    return f"postgresql://{quote(str(username), safe='')}:{quote(str(password), safe='')}@{host}:{port}/{quote(str(database), safe='')}"


