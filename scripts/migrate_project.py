def parse_percentage_limit(value: str) -> float | None:
    text = str(value).strip().rstrip("%")
    if not text:
        return None
    try:
        parsed = float(text)
    except ValueError:
        return None
    if parsed > 1:
        parsed = parsed / 100
    if parsed <= 0:
        return None
    return parsed


